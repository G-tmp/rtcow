
'use strict';


const startButton = document.getElementById('startButton');
const hangupButton = document.getElementById('hangupButton');
hangupButton.disabled = true;

const localVideo = document.getElementById('localVideo');
const remoteVideo = document.getElementById('remoteVideo');
const errorMsg = document.getElementById('errorMsg');
const httpAddress = document.getElementById('httpAddr');

let pc;
let localStream;

let host = "127.0.0.1:12345";
if (window.location.hostname !== "127.0.0.1") {
  host ="192.168.101.86:12345";
}
let httpAddr = "https://" + host;
let wsAddr = "wss://" + host + "/ws";

httpAddress.href = httpAddr;
httpAddress.innerHTML = httpAddr;
const ws = new WebSocket(wsAddr);

ws.onerror = (err) => {
  showError(`${wsAddr} connect faild`)
  console.log(err)
}

ws.onmessage = (e) => {
  let data = JSON.parse(e.data)

  if (!localStream) {
    console.log('not ready yet');
    return;
  }
  console.log(data)
  switch (data.type) {
    case 'offer':
      handleOffer(data);
      break;
    case 'answer':
      handleAnswer(data);
      break;
    case 'candidate':
      handleCandidate(data);
      break;
    case 'ready':
      // A second tab joined. This tab will initiate a call unless in a call already.
      if (pc) {
        console.log('already in call, ignoring');
        return;
      }
      makeCall();
      break;
    case 'bye':
      if (pc) {
        hangup();
      }
      break;
    default:
      console.log('unhandled', data);
      break;
  }
}

startButton.onclick = async () => {
  localStream = await navigator.mediaDevices.getUserMedia({audio: true, video: true});
  localVideo.srcObject = localStream;


  startButton.disabled = true;
  hangupButton.disabled = false;

  ws.send(JSON.stringify({type: 'ready'}));
};

hangupButton.onclick = async () => {
  hangup();
  ws.send(JSON.stringify({type: 'bye'}));
};

async function hangup() {
  if (pc) {
    pc.close();
    pc = null;
  }
  localStream.getTracks().forEach(track => track.stop());
  localStream = null;
  startButton.disabled = false;
  hangupButton.disabled = true;
};

function createPeerConnection() {
  pc = new RTCPeerConnection();
  pc.onicecandidate = e => {
    const message = {
      type: 'candidate',
      candidate: null,
    };
    if (e.candidate) {
      message.candidate = e.candidate.candidate;
      message.sdpMid = e.candidate.sdpMid;
      message.sdpMLineIndex = e.candidate.sdpMLineIndex;
    }
    ws.send(JSON.stringify(message));
  };
  pc.ontrack = e => remoteVideo.srcObject = e.streams[0];
  localStream.getTracks().forEach(track => pc.addTrack(track, localStream));
}

async function makeCall() {
  await createPeerConnection();

  const offer = await pc.createOffer();
  ws.send(JSON.stringify({type: 'offer', sdp: offer.sdp}));
  await pc.setLocalDescription(offer);
}

async function handleOffer(offer) {
  if (pc) {
    console.error('existing peerconnection');
    return;
  }
  await createPeerConnection();
  await pc.setRemoteDescription(offer);

  const answer = await pc.createAnswer();
  ws.send(JSON.stringify({type: 'answer', sdp: answer.sdp}));
  await pc.setLocalDescription(answer);
}

async function handleAnswer(answer) {
  if (!pc) {
    console.error('no peerconnection');
    return;
  }
  await pc.setRemoteDescription(answer);
}

async function handleCandidate(candidate) {
  if (!pc) {
    console.error('no peerconnection');
    return;
  }
  if (!candidate.candidate) {
    await pc.addIceCandidate(null);
  } else {
    await pc.addIceCandidate(candidate);
  }
}

function showError(e) {
  errorMsg.innerHTML += `<p>${e}</p>`;
}